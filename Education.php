                            <P> Working Group for Inclusive Education engages in research based analysis, monitoring policy-making processes and evidence based advocacy for reforms in education sector particularly relating to curriculum, education policy and textbooks.
                                The Working Group engages in developing perspectives through discussions, dialogue with policy makers and influencers, to draw attention to facilitate inclusive, equitable and quality learning. 
                                The Working Group is composed of eminent educationists, researchers and policy experts from Pakistan. The Centre for Social Justice facilitates functions and coordination among the Working Group since its inception in December 2018. 
                            </P>

